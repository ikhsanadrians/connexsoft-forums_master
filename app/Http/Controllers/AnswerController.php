     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
     {

         $user = Auth::user()->id;
         $answers = $request->contents;
         $questionid = $request->route('id');


         Answer::create([
           'user_id' => $user,
           'content' => $answers,
           'question_id'=> $questionid,

         ]);

         return redirect(route('detailsquestion',$questionid));
    }

